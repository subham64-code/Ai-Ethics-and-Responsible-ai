FN = "Nirmala"
def set_font(run):
    run.font.name = FN
    run.element.rPr.rFonts.set(qn("w:eastAsia"), FN)

def add_head(text, lv=1):
    h = doc.add_heading(text, level=lv)
    for r in h.runs:
        set_font(r)
    return h

def add_p(text, bold=False, size=12, color=None, align=None):
    p = doc.add_paragraph()
    if align: p.alignment = align
    run = p.add_run(text)
    set_font(run)
    run.font.size = Pt(size)
    if bold: run.bold = True
    if color: run.font.color.rgb = color
    return p

def add_bullet(text):
    p = doc.add_paragraph(style="List Bullet")
    run = p.add_run(text)
    set_font(run)
    return p

def add_img(path, w=5.0, cap=None):
    if os.path.exists(path):
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.add_run()
        run.add_picture(path, width=Inches(w))
        if cap:
            p2 = doc.add_paragraph()
            p2.alignment = WD_ALIGN_PARAGRAPH.CENTER
            r = p2.add_run(cap)
            set_font(r)
            r.font.size = Pt(9)
            r.italic = True

def add_tab(headers, rows):
    t = doc.add_table(rows=len(rows)+1, cols=len(headers), style="Light Grid Accent 1")
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, h in enumerate(headers):
        c = t.rows[0].cells[i]; c.text = h
        for p in c.paragraphs:
            for r in p.runs:
                r.bold = True; set_font(r)
    for ri, rd in enumerate(rows, 1):
        for ci, v in enumerate(rd):
            c = t.rows[ri].cells[ci]; c.text = str(v)
            for p in c.paragraphs:
                for r in p.runs:
                    set_font(r)

